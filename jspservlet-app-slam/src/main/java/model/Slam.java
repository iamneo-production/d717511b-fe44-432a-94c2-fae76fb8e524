package model;

public class Slam {

    private int slamId;
    private String toWhom;
    private String aboutThem;
    private String bestMemories;
    
    public int getSlamId() {
        return slamId;
    }
    public void setSlamId(int slamId) {
        this.slamId = slamId;
    }
    public String getToWhom() {
        return toWhom;
    }
    public void setToWhom(String toWhom) {
        this.toWhom = toWhom;
    }
    public String getAboutThem() {
        return aboutThem;
    }
    public void setAboutThem(String aboutThem) {
        this.aboutThem = aboutThem;
    }
    public String getBestMemories() {
        return bestMemories;
    }
    public void setBestMemories(String bestMemories) {
        this.bestMemories = bestMemories;
    }

    
}